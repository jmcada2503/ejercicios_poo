    private void btn_calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calcularActionPerformed
        Empleado myEmployee = new Empleado(txt_nombre.getText(), Double.parseDouble(txt_valor_hora.getText()), Double.parseDouble(txt_horas.getText()));
        if (myEmployee.salary() > 450000) {
            txt_salario.setText(String.valueOf(myEmployee.salary()));
        }
        else {
            javax.swing.JOptionPane.showMessageDialog(null, "El salario del empleado es menor o igual a 450000");
        }
    }//GEN-LAST:event_btn_calcularActionPerformed

