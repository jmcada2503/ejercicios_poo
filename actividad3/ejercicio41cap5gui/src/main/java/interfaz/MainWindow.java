    private void btn_ordenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ordenarActionPerformed
        String[] input = txt_values.getText().split(",");
        double[] values = new double[input.length];
        for (int i=0; i<input.length; i++) {
            values[i] = Double.parseDouble(input[i]);
        }

        NumberSeries series = new NumberSeries(values);
        series.orderSeries();
        txt_values.setText(series.getCsvString());
        txt_mayor.setText(String.valueOf(series.values[series.values.length-1]));
    }//GEN-LAST:event_btn_ordenarActionPerformed

