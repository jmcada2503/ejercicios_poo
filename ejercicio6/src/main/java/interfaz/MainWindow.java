    private void btn_crearActionPerformed(java.awt.event.ActionEvent evt) throws IOException {//GEN-FIRST:event_btn_crearActionPerformed
        this.last_song = new Canciones(this.txt_nombre.getText(), this.txt_genero.getText(), this.txt_artista.getText(), Integer.parseInt(this.txt_duracion.getText()));

        this.crud.create(
                this.last_song.nombre,
                this.last_song.genero,
                this.last_song.artista,
                this.last_song.duracion
                );
    }//GEN-LAST:event_btn_crearActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) throws IOException {//GEN-FIRST:event_btn_eliminarActionPerformed
        this.crud.delete(this.last_song.nombre);

        this.txt_nombre.setText("");
        this.txt_genero.setText("");
        this.txt_artista.setText("");
        this.txt_duracion.setText("");
    }//GEN-LAST:event_btn_eliminarActionPerformed

    private void btn_limpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_limpiarActionPerformed
        this.txt_nombre.setText("");
        this.txt_genero.setText("");
        this.txt_artista.setText("");
        this.txt_duracion.setText("");
    }//GEN-LAST:event_btn_limpiarActionPerformed

