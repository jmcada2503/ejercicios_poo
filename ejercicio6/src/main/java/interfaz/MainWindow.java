    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) throws IOException {//GEN-FIRST:event_btn_buscarActionPerformed
        this.last_song = this.crud.read(this.txt_nombre.getText());

        this.txt_nombre.setText(last_song.nombre);
        this.txt_genero.setText(last_song.genero);
        this.txt_artista.setText(last_song.artista);
        this.txt_duracion.setText(String.valueOf(last_song.duracion));
    }//GEN-LAST:event_btn_buscarActionPerformed

    private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) throws IOException {//GEN-FIRST:event_btn_actualizarActionPerformed
        this.crud.edit(
                this.last_song.nombre,
                this.txt_nombre.getText(),
                this.txt_genero.getText(),
                this.txt_artista.getText(),
                Integer.parseInt(this.txt_duracion.getText())
                );

        this.last_song = this.crud.read(this.txt_nombre.getText());
     }//GEN-LAST:event_btn_actualizarActionPerformed

